  <div id="aide_article">
      <article>
         <h3>La jfd</h3>
         <p>La journée de la femme digitale (JFD) est le rendez-vous féminin de l'innovation. L'objectif : mettre à l'honneur les femmes chefs d'entreprise ou occupant des postes de direction dans les nouvelles technologies.
            <br><br>Les lauréates bénéficieront de l'exposition médiatique de la JFD, de soutiens financiers, d’une valeur équivalente à 1 millions d’euros.</p>
      </article>
      <article>
         <h3>Journée des femmes entrepreneuses</h3>
         <p>La journée des femmes entrepreneures est organisée chaque année en marge du Salon SME, un événement ayant lieu à Paris et dédié aux dirigeants et créateurs de start-ups ou de TPE/PME.
            <br><br>La journée de la femme entrepreneure propose durant une journée de nombreuses conférences et ateliers pour encourager et accompagner les femmes porteuses d’un projet.</p>
      </article>
      <article>
         <h3>Prêt d’honneur</h3>
         <p>Il s’agit d’un prêt d’honneur accordé sans demande de garantie personnelle ni intérêts par le réseau Initiative France. Il permet aux femmes entrepreneures de renforcer leurs fonds propres et ainsi d’accéder plus facilement à des prêts plus importants. Sans remplacer l’emprunt, il permet d’en simplifier la démarche grâce à un effet de levier.
</p>
      </article>
